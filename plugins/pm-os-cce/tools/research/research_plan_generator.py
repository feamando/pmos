"""
PM-OS CCE ResearchPlanGenerator (v5.0)

Creates structured two-pass research plans for deep feature discovery.
Given questionnaire answers and available sources, generates internal
(Brain, Jira, GitHub, Confluence, etc.) and external (web, competitor,
market validation) research tasks. Config-driven source detection.

Usage:
    from pm_os_cce.tools.research.research_plan_generator import (
        ResearchPlanGenerator, ResearchPlan
    )
"""

import logging
import os
from dataclasses import dataclass, field
from datetime import datetime
from enum import Enum
from pathlib import Path
from typing import Any, Dict, List, Optional

try:
    from pm_os_base.tools.core.config_loader import get_config
except ImportError:
    from core.config_loader import get_config

try:
    from pm_os_base.tools.core.path_resolver import get_paths
except ImportError:
    from core.path_resolver import get_paths

try:
    from pm_os_cce.tools.research.discovery_questionnaire import QuestionnaireResult
except ImportError:
    from research.discovery_questionnaire import QuestionnaireResult

logger = logging.getLogger(__name__)


class ResearchSource(Enum):
    """Available research sources for the deep research swarm."""

    BRAIN = "brain"
    MASTER_SHEET = "master_sheet"
    JIRA = "jira"
    GITHUB = "github"
    CONFLUENCE = "confluence"
    GDOCS = "gdocs"
    SLACK = "slack"
    STATSIG = "statsig"
    WEB = "web"
    GEMINI_DEEP_RESEARCH = "gemini_deep_research"
    CODEBASE = "codebase"
    FIGMA = "figma"


class SourceStatus(Enum):
    """Connectivity status for a research source."""

    AVAILABLE = "available"
    CONFIGURED = "configured"
    UNAVAILABLE = "unavailable"


@dataclass
class SourceProbe:
    """Result of probing a single source for availability."""

    source: ResearchSource
    status: SourceStatus
    reason: str = ""

    def to_dict(self) -> Dict[str, Any]:
        return {
            "source": self.source.value,
            "status": self.status.value,
            "reason": self.reason,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "SourceProbe":
        return cls(
            source=ResearchSource(data["source"]),
            status=SourceStatus(data["status"]),
            reason=data.get("reason", ""),
        )


@dataclass
class ResearchTask:
    """
    A single research task within the research plan.

    Attributes:
        id: Unique task identifier (e.g., "int-brain-01")
        source: Which source to search
        query: The search query or prompt
        purpose: Why this search is needed
        pass_number: 1 for broad first pass, 2 for targeted follow-up
        category: "internal" or "external"
        estimated_seconds: Rough time estimate
        depends_on: Task IDs that must complete before this one
    """

    id: str
    source: ResearchSource
    query: str
    purpose: str
    pass_number: int = 1
    category: str = "internal"
    estimated_seconds: int = 30
    depends_on: Optional[List[str]] = None

    def to_dict(self) -> Dict[str, Any]:
        result = {
            "id": self.id,
            "source": self.source.value,
            "query": self.query,
            "purpose": self.purpose,
            "pass_number": self.pass_number,
            "category": self.category,
            "estimated_seconds": self.estimated_seconds,
        }
        if self.depends_on:
            result["depends_on"] = self.depends_on
        return result

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "ResearchTask":
        return cls(
            id=data["id"],
            source=ResearchSource(data["source"]),
            query=data["query"],
            purpose=data["purpose"],
            pass_number=data.get("pass_number", 1),
            category=data.get("category", "internal"),
            estimated_seconds=data.get("estimated_seconds", 30),
            depends_on=data.get("depends_on"),
        )


@dataclass
class ResearchPlan:
    """
    A structured research plan with internal and external tasks.

    Generated by ResearchPlanGenerator and executed by DeepResearchSwarm.
    """

    feature_title: str
    questionnaire_summary: Dict[str, Any] = field(default_factory=dict)
    available_sources: List[SourceProbe] = field(default_factory=list)
    internal_tasks: List[ResearchTask] = field(default_factory=list)
    external_tasks: List[ResearchTask] = field(default_factory=list)
    first_pass_hits: Dict[str, int] = field(default_factory=dict)
    estimated_total_seconds: int = 0
    created_at: datetime = field(default_factory=datetime.now)
    approved: bool = False
    challenged: bool = False
    challenge_feedback: Optional[str] = None

    @property
    def total_tasks(self) -> int:
        return len(self.internal_tasks) + len(self.external_tasks)

    @property
    def pass_1_tasks(self) -> List[ResearchTask]:
        return [
            t
            for t in self.internal_tasks + self.external_tasks
            if t.pass_number == 1
        ]

    @property
    def pass_2_tasks(self) -> List[ResearchTask]:
        return [
            t
            for t in self.internal_tasks + self.external_tasks
            if t.pass_number == 2
        ]

    def to_dict(self) -> Dict[str, Any]:
        return {
            "feature_title": self.feature_title,
            "questionnaire_summary": self.questionnaire_summary,
            "available_sources": [s.to_dict() for s in self.available_sources],
            "internal_tasks": [t.to_dict() for t in self.internal_tasks],
            "external_tasks": [t.to_dict() for t in self.external_tasks],
            "first_pass_hits": self.first_pass_hits,
            "estimated_total_seconds": self.estimated_total_seconds,
            "created_at": self.created_at.isoformat(),
            "approved": self.approved,
            "challenged": self.challenged,
            "challenge_feedback": self.challenge_feedback,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "ResearchPlan":
        return cls(
            feature_title=data.get("feature_title", ""),
            questionnaire_summary=data.get("questionnaire_summary", {}),
            available_sources=[
                SourceProbe.from_dict(s)
                for s in data.get("available_sources", [])
            ],
            internal_tasks=[
                ResearchTask.from_dict(t)
                for t in data.get("internal_tasks", [])
            ],
            external_tasks=[
                ResearchTask.from_dict(t)
                for t in data.get("external_tasks", [])
            ],
            first_pass_hits=data.get("first_pass_hits", {}),
            estimated_total_seconds=data.get("estimated_total_seconds", 0),
            created_at=datetime.fromisoformat(
                data.get("created_at", datetime.now().isoformat())
            ),
            approved=data.get("approved", False),
            challenged=data.get("challenged", False),
            challenge_feedback=data.get("challenge_feedback"),
        )


