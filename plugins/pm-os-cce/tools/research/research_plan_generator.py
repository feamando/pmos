def _build_search_terms(questionnaire: QuestionnaireResult) -> str:
    """Build search query from questionnaire answers."""
    parts = []
    if questionnaire.feature_description:
        desc = questionnaire.feature_description
        if "." in desc[:100]:
            parts.append(desc[: desc.index(".") + 1])
        else:
            parts.append(desc[:100])
    elif questionnaire.user_problem:
        parts.append(questionnaire.user_problem[:100])

    if questionnaire.product:
        parts.append(questionnaire.product)
    elif questionnaire.brand:
        parts.append(questionnaire.brand)

    return " ".join(parts).strip()


def _build_competitor_query(questionnaire: QuestionnaireResult) -> str:
    """Build a competitor-focused search query."""
    parts = []
    if questionnaire.competitors:
        parts.append(" vs ".join(questionnaire.competitors[:3]))
    if questionnaire.feature_description:
        parts.append(questionnaire.feature_description[:80])
    if questionnaire.brand:
        parts.append(questionnaire.brand)
    return " ".join(parts).strip()
